 do{
    let result = i * 7;
    console.log(`7 x ${i} = ${result}`);
    i++;
    }while(i <= 10);
    for(let num = 1; num <= 10; num++){
        let result = num * 7;
        console.log(`7 x ${num} = ${result}`);
}   